#!/bin/bash

# Check if .env file exists
if [ -f .env ]; then
    # export all variables defined in the .env file
    set -a
    source .env
    set +a
else
    echo ".env file not found!"
    exit 1
fi

DATASETS=("arxiv2" "papers100M" "products" "coauthor")

# Ensure the temp directory exists and is writable by neo4j
mkdir -p /var/lib/neo4j/data/tmp
chown -R neo4j:neo4j /var/lib/neo4j/data/tmp

cd /var/lib/neo4j/data

for DS in "${DATASETS[@]}"; do
    echo "--------------------------------------------"
    echo "Starting compaction process for database: $DS"
    echo "--------------------------------------------"

    # Stop the database first so files can be safely copied
    echo "Stopping database $DS..."
    cypher-shell -u "$USERNAME" -p "$PASSWORD" -d system "STOP DATABASE $DS;"
    sleep 5 # Give the DBMS a moment to cleanly unallocate the store

    # Run the compaction as the neo4j user to preserve file permissions
    echo "Running store compaction..."
    sudo -u neo4j neo4j-admin database copy "$DS" "$DS" --compact-node-store --temp-path=/var/lib/neo4j/data/tmp

    # Start the database back up
    echo "Starting database $DS..."
    cypher-shell -u "$USERNAME" -p "$PASSWORD" -d system "START DATABASE $DS;"
    sleep 5

    # Apply the schema file generated by the compaction tool
    # Note: This file is dropped into the directory where you execute this script.
    if [ -f "${DS}-schema.cypher" ]; then
        echo "Applying schema and recreating indexes for $DS..."
        cypher-shell -u "$USERNAME" -p "$PASSWORD" -d "$DS" -f "${DS}-schema.cypher"
        # Optional: Clean up the schema file afterward
        rm "${DS}-schema.cypher"
    else
        echo "Warning: Schema file ${DS}-schema.cypher not found. Skipping index recreation."
    fi

done
